    def board_hit_test(self, pos) -> Optional[Arrow]:
        if not self.game:
            return None
        best = None
        best_dist = 1e9
        for a in self.game["arrows"]:
            path = a.path
            for i in range(len(path) - 1):
                ax, ay = self.point_pos(path[i]["r"], path[i]["c"])
                bx, by = self.point_pos(path[i + 1]["r"], path[i + 1]["c"])
                d = self.dist_point_segment(pos[0], pos[1], ax, ay, bx, by)
                if d < best_dist:
                    best_dist = d
                    best = a
        return best if best_dist < max(15, 16 * self.zoom) else None

    @staticmethod
    def dist_point_segment(px, py, ax, ay, bx, by):
        dx, dy = bx - ax, by - ay
        l2 = dx * dx + dy * dy
        if l2 == 0:
            return math.hypot(px - ax, py - ay)
        t = max(0, min(1, ((px - ax) * dx + (py - ay) * dy) / l2))
        x, y = ax + t * dx, ay + t * dy
        return math.hypot(px - x, py - y)

    def handle_arrow_click(self, arrow: Arrow) -> None:
        if not self.game or self.game["status"] != "playing" or self.animation:
            return
        if not self.cheat and not self.game["started"]:
            self.game["started"] = True
        self.highlighted = None
        if self.game["erase_mode"]:
            self.remove_arrow(arrow)
            if self.game["erases"] != math.inf:
                self.game["erases"] -= 1
            self.game["erase_mode"] = False
            self.message = "已擦除一条箭头。"
            self.after_move_check()
            return
        if can_move(arrow, self.game["occupancy"]):
            self.animation = {"type": "exit", "arrow_id": arrow.id, "t": 0.0, "duration": min(0.95, 0.42 + len(arrow.path) * 0.018)}
            self.message = "成功移除。"
        else:
            self.animation = {"type": "bump", "arrow_id": arrow.id, "t": 0.0, "duration": 0.28}
            if self.cheat:
                self.message = "前方被挡住了，但 Cheat Mode 不扣心。"
            else:
                self.game["hearts"] -= 1
                self.message = "前方被挡住了，扣 1 颗心。"
                if self.game["hearts"] <= 0:
                    self.game["status"] = "lose"
                    self.message = "Game Over"

    def use_hint(self) -> None:
        if not self.game or self.game["status"] != "playing" or self.game["hints"] == 0:
            return
        movable = [a for a in self.game["arrows"] if can_move(a, self.game["occupancy"])]
        if not movable:
            self.message = "当前局面没有可移动箭头，可能需要擦除或重开。"
            return
        movable.sort(key=lambda a: len(a.path))
        self.highlighted = movable[0].id
        self.flash_timer = 3.2
        if self.game["hints"] != math.inf:
            self.game["hints"] -= 1
        self.message = "黄色高亮的是当前可以移除的一条箭头。"

    def toggle_erase(self) -> None:
        if not self.game or self.game["status"] != "playing" or self.game["erases"] == 0:
            return
        self.game["erase_mode"] = not self.game["erase_mode"]
        self.highlighted = None
        self.message = "擦除模式：点击任意箭头直接删除。" if self.game["erase_mode"] else "已取消擦除模式。"

    def handle_action(self, action: str) -> None:
        if action == "levels":
            self.state = LEVELS
        elif action == "random":
            self.start_random()
        elif action == "seed":
            self.input_text = ""
            self.input_error = ""
            try:
                pygame.key.start_text_input()
            except Exception:
                pass
            self.state = SEED_INPUT
        elif action == "help":
            self.prev_state = self.state
            self.state = HELP
        elif action == "admin":
            self.open_admin()
        elif action == "reset":
            self.prev_state = self.state
            self.state = RESET_CONFIRM
        elif action == "menu" or action == "back":
            self.game = None
            self.state = MENU
        elif action == "restart":
            if self.game:
                self.setup_game_from_seed(self.game["original_seed"], self.game["mode"], self.game["level_index"])
        elif action == "grid":
            if self.game:
                self.game["grid_on"] = not self.game["grid_on"]
        elif action == "hint":
            self.use_hint()
        elif action == "erase":
            self.toggle_erase()
        elif action == "next_level":
            if self.game and self.game["level_index"] is not None and self.game["level_index"] < 99:
                self.start_level(self.game["level_index"] + 1)
        elif action == "random_retry":
            self.retry_same_random()
        elif action == "random_new":
            self.start_random()
        elif action.startswith("level:"):
            self.start_level(int(action.split(":", 1)[1]))
        elif action == "dialog_cancel":
            self.state = self.prev_state if self.state in (ADMIN_INPUT, RESET_CONFIRM, HELP) else MENU
        elif action == "dialog_ok":
            if self.state == SEED_INPUT:
                try:
                    self.setup_game_from_seed(self.input_text, "seed")
                except Exception as e:
                    self.input_error = str(e)
                    self.state = SEED_INPUT
            elif self.state == ADMIN_INPUT:
                if self.input_text.strip().lower() == "admin":
                    self.cheat = True
                    self.menu_message = "作弊模式已开启：关卡全解锁、不限时、技能无限。"
                    self.state = MENU
                else:
                    self.input_error = "口令错误。"
            elif self.state == RESET_CONFIRM:
                clear_save()
                self.unlocked = 1
                self.done = {}
                self.menu_message = "进度已清空。"
                self.state = MENU
            elif self.state == HELP:
                self.state = MENU


    def paste_clipboard_text(self) -> str:
        """Return plain clipboard text for pygame input dialogs.

        This is deliberately over-compatible for Windows/Pygame builds:
        pygame.scrap works on some systems, tkinter works on many Python installs,
        and the WinAPI fallback works for packaged one-file exe builds where tkinter
        may be unavailable.
        """
        text = ""

        # 1) pygame clipboard/scrap
        try:
            pygame.scrap.init()
            formats = []
            for name in ("SCRAP_TEXT", "SCRAP_UTF8", "SCRAP_TEXT_UTF8"):
                if hasattr(pygame, name):
                    formats.append(getattr(pygame, name))
            formats += ["text/plain;charset=utf-8", "text/plain", "UTF8_STRING"]
            for fmt in formats:
                try:
                    data = pygame.scrap.get(fmt)
                except Exception:
                    data = None
                if data:
                    if isinstance(data, bytes):
                        for enc in ("utf-8", "utf-16-le", "gbk", "latin1"):
                            try:
                                text = data.decode(enc).strip("\x00")
                                break
                            except Exception:
                                continue
                    else:
                        text = str(data)
                    if text:
                        break
        except Exception:
            pass

        # 2) tkinter fallback
        if not text:
            try:
                import tkinter as tk
                root = tk.Tk()
                root.withdraw()
                root.update()
                text = root.clipboard_get()
                root.destroy()
            except Exception:
                text = ""

        # 3) WinAPI Unicode clipboard fallback
        if not text:
            try:
                import ctypes
                user32 = ctypes.windll.user32
                kernel32 = ctypes.windll.kernel32
                CF_UNICODETEXT = 13
                if user32.OpenClipboard(None):
                    handle = user32.GetClipboardData(CF_UNICODETEXT)
                    if handle:
                        kernel32.GlobalLock.restype = ctypes.c_void_p
                        ptr = kernel32.GlobalLock(handle)
                        if ptr:
                            text = ctypes.wstring_at(ptr)
                            kernel32.GlobalUnlock(handle)
                    user32.CloseClipboard()
            except Exception:
                text = ""

        # 4) PowerShell fallback, useful for some Windows console/python installs
        if not text:
            try:
                import subprocess
                cp = subprocess.run(
                    ["powershell", "-NoProfile", "-Command", "Get-Clipboard"],
                    capture_output=True,
                    text=True,
                    timeout=2,
                    creationflags=0x08000000 if hasattr(__import__('subprocess'), 'CREATE_NO_WINDOW') else 0,
                )
                if cp.returncode == 0:
                    text = cp.stdout
            except Exception:
                text = ""

        return text.replace("\x00", "").replace("\r", "").replace("\n", "")

    def clean_dialog_text(self, text: str) -> str:
        """Filter pasted/typed text according to the active dialog.

        Only ASCII seed/admin characters are accepted.  This avoids Chinese IME
        composition text entering the field accidentally.
        """
        if self.state == SEED_INPUT:
            allowed = set("ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789-")
            return "".join(ch.upper() for ch in text if ch in allowed)[:420]
        if self.state == ADMIN_INPUT:
            allowed = set("ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789")
            return "".join(ch.lower() for ch in text if ch in allowed)[:32]
        return text

    def key_to_dialog_char(self, event) -> str:
        """Manual key mapping for pygame text input.

        Pygame fields are not native OS text boxes, so we do not assume that
        event.unicode always works.  Every letter key and number key is mapped
        explicitly, then TEXTINPUT/event.unicode are used only as fallbacks.
        """
        k = event.key
        mods = getattr(event, "mod", 0) | pygame.key.get_mods()

        # Explicit A-Z mapping.  Some Windows/IME layouts do not provide
        # useful event.unicode for alphabet keys, but event.key is still stable.
        letters = {
            pygame.K_a: "a", pygame.K_b: "b", pygame.K_c: "c", pygame.K_d: "d",
            pygame.K_e: "e", pygame.K_f: "f", pygame.K_g: "g", pygame.K_h: "h",
            pygame.K_i: "i", pygame.K_j: "j", pygame.K_k: "k", pygame.K_l: "l",
            pygame.K_m: "m", pygame.K_n: "n", pygame.K_o: "o", pygame.K_p: "p",
            pygame.K_q: "q", pygame.K_r: "r", pygame.K_s: "s", pygame.K_t: "t",
            pygame.K_u: "u", pygame.K_v: "v", pygame.K_w: "w", pygame.K_x: "x",
            pygame.K_y: "y", pygame.K_z: "z",
        }
        if k in letters:
            ch = letters[k]
            caps = getattr(pygame, "KMOD_CAPS", 0)
            if self.state == SEED_INPUT or (mods & pygame.KMOD_SHIFT) or (caps and (mods & caps)):
                ch = ch.upper()
            return ch

        number_row = {
            pygame.K_0: "0", pygame.K_1: "1", pygame.K_2: "2", pygame.K_3: "3", pygame.K_4: "4",
            pygame.K_5: "5", pygame.K_6: "6", pygame.K_7: "7", pygame.K_8: "8", pygame.K_9: "9",
        }
        if k in number_row:
            return number_row[k]

        numpad = {
            pygame.K_KP0: "0", pygame.K_KP1: "1", pygame.K_KP2: "2", pygame.K_KP3: "3", pygame.K_KP4: "4",
            pygame.K_KP5: "5", pygame.K_KP6: "6", pygame.K_KP7: "7", pygame.K_KP8: "8", pygame.K_KP9: "9",
        }
        if k in numpad:
            return numpad[k]

        if k in (pygame.K_MINUS, pygame.K_KP_MINUS):
            return "-" if self.state == SEED_INPUT else ""

        uni = getattr(event, "unicode", "") or ""
        # Ignore control characters such as Ctrl+V (^V).  Paste is handled before this method.
        if len(uni) == 1 and ord(uni) >= 32:
            return uni
        return ""

    def append_dialog_text(self, text: str) -> None:
        cleaned = self.clean_dialog_text(text)
        if not cleaned:
            return
        limit = 420 if self.state == SEED_INPUT else 32
        self.input_text = (self.input_text + cleaned)[:limit]

    def is_paste_shortcut(self, event) -> bool:
        mods = getattr(event, "mod", 0) | pygame.key.get_mods()
        ctrl = mods & (pygame.KMOD_CTRL | pygame.KMOD_META)
        uni = getattr(event, "unicode", "") or ""
        return bool(ctrl and (event.key == pygame.K_v or uni.lower() == "v" or uni == "\x16"))

    def handle_events(self) -> bool:
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                return False
            if self.state in (SEED_INPUT, ADMIN_INPUT) and event.type == pygame.TEXTINPUT:
                # TEXTINPUT is the single source of normal typed characters.
                # This avoids KEYDOWN + TEXTINPUT adding the same letter/number twice.
                # Pasted text sometimes also arrives as TEXTINPUT after Ctrl+V KEYDOWN;
                # skip that one event after we have already pasted from the clipboard.
                if self.skip_next_textinput:
                    self.skip_next_textinput = False
                    continue
                self.append_dialog_text(getattr(event, "text", ""))
                continue
            if event.type == pygame.KEYDOWN:
                if self.state in (SEED_INPUT, ADMIN_INPUT):
                    if event.key == pygame.K_ESCAPE:
                        self.state = MENU
                    elif event.key == pygame.K_RETURN or event.key == pygame.K_KP_ENTER:
                        self.handle_action("dialog_ok")
                    elif event.key == pygame.K_BACKSPACE:
                        self.input_text = self.input_text[:-1]
                    elif event.key == pygame.K_DELETE:
                        self.input_text = ""
                    elif self.is_paste_shortcut(event):
                        self.append_dialog_text(self.paste_clipboard_text())
                        self.skip_next_textinput = True
                    else:
                        # Do not add printable KEYDOWN characters here, because pygame 2
                        # also sends TEXTINPUT for the same key.  Keep a narrow fallback
                        # only for old pygame builds without TEXTINPUT support.
                        if not hasattr(pygame, "TEXTINPUT"):
                            self.append_dialog_text(self.key_to_dialog_char(event))
                    continue
                if event.key == pygame.K_ESCAPE:
                    if self.state == GAME:
                        self.game = None
                        self.state = MENU
                    elif self.state != MENU:
                        self.state = MENU
                    else:
                        return False
                elif event.key == pygame.K_r and self.state == GAME and self.game:
                    self.setup_game_from_seed(self.game["original_seed"], self.game["mode"], self.game["level_index"])
            if event.type == pygame.MOUSEWHEEL and self.state == GAME:
                self.zoom = max(0.72, min(1.35, self.zoom + event.y * 0.06))
            if event.type == pygame.MOUSEBUTTONDOWN and event.button == 1:
                pos = event.pos
                for b in list(self.buttons):
                    if b.enabled and b.rect.collidepoint(pos):
                        self.handle_action(b.action)
                        break
                else:
                    if self.state == GAME and self.game and self.game["status"] == "playing":
                        arrow = self.board_hit_test(pos)
                        if arrow:
                            self.handle_arrow_click(arrow)
        return True

    def update(self, dt: float) -> None:
        if self.flash_timer > 0:
            self.flash_timer -= dt
        if self.animation:
            self.animation["t"] += dt / self.animation["duration"]
            if self.animation["t"] >= 1:
                finished = self.animation
                self.animation = None
                if finished["type"] == "exit":
                    a = self.find_arrow(finished["arrow_id"])
                    if a:
                        self.remove_arrow(a)
                    self.after_move_check()
        if self.game and self.game["status"] == "playing" and not self.cheat and self.game["started"]:
            self.game["time_left"] -= dt
            if self.game["time_left"] <= 0:
                self.game["time_left"] = 0
                self.game["status"] = "lose"
                self.message = "时间到。"

    def run(self) -> None:
        running = True
        while running:
            dt = min(0.05, self.clock.tick(60) / 1000.0)
            running = self.handle_events()
            self.update(dt)
            self.draw()
        pygame.quit()


