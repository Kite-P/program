def app_dir() -> str:
    if getattr(sys, "frozen", False):
        return os.path.dirname(sys.executable)
    return os.path.dirname(os.path.abspath(__file__))


def save_path() -> str:
    return os.path.join(app_dir(), SAVE_FILE)


def safe_load_save() -> dict:
    try:
        with open(save_path(), "r", encoding="utf-8") as f:
            data = json.load(f)
        if not isinstance(data, dict):
            raise ValueError
        return data
    except Exception:
        return {"unlocked": 1, "done": {}}


def write_save(unlocked: int, done: dict) -> None:
    try:
        with open(save_path(), "w", encoding="utf-8") as f:
            json.dump({"unlocked": int(unlocked), "done": done}, f, ensure_ascii=False, indent=2)
    except Exception:
        pass


def clear_save() -> None:
    try:
        if os.path.exists(save_path()):
            os.remove(save_path())
    except Exception:
        pass


def idx(r: int, c: int) -> int:
    return r * COLS + c


def in_bounds(r: int, c: int) -> bool:
    return 0 <= r < ROWS and 0 <= c < COLS


def code_of(head_side: str, tail_side: str) -> int:
    return DIRS.index(head_side) * 5 + DIRS.index(tail_side)


def dirs_of(v: int) -> tuple[str, str]:
    return DIRS[v // 5], DIRS[v % 5]


def dir_between(a: dict, b: dict) -> Optional[str]:
    dr = b["r"] - a["r"]
    dc = b["c"] - a["c"]
    if dr == -1 and dc == 0:
        return "u"
    if dr == 1 and dc == 0:
        return "d"
    if dr == 0 and dc == -1:
        return "l"
    if dr == 0 and dc == 1:
        return "r"
    return None


def seed_key(limit_time: int, i: int) -> int:
    return (limit_time + i * 7 + 13) % 25


def checksum(text: str) -> str:
    h = 2166136261
    for ch in text:
        h ^= ord(ch)
        h = (h * 16777619) & 0xFFFFFFFF
    value = h % (36 ** 4)
    chars = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ"
    out = ""
    for _ in range(4):
        out = chars[value % 36] + out
        value //= 36
    return out.rjust(4, "0")


def group_seed(raw: str) -> str:
    return "-".join(raw[i:i + 4] for i in range(0, len(raw), 4))


def normalize_seed(seed: str) -> str:
    return "".join(ch for ch in str(seed).upper() if ch.isalnum())


def to_base36(n: int) -> str:
    chars = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ"
    if n == 0:
        return "0"
    out = ""
    while n:
        out = chars[n % 36] + out
        n //= 36
    return out


def encode_seed(grid: list[int], limit_time: int) -> str:
    t = max(300, min(900, round(limit_time)))
    time_part = to_base36(t).upper().rjust(3, "0")
    body = ""
    for i, v in enumerate(grid):
        body += ALPHABET[(v + seed_key(t, i)) % 25]
    check = checksum(time_part + body)
    return group_seed(time_part + body + check)


def validate_grid_values(grid: list[int]) -> None:
    if not isinstance(grid, list) or len(grid) != POINTS:
        raise ValueError("格点数据长度不正确。")
    for v in grid:
        if not isinstance(v, int) or not 0 <= v < 25:
            raise ValueError("Seed 中存在非法格点值。")
        h, t = dirs_of(v)
        if h == "n" and t == "n":
            continue
        if h == "n" and t != "n":
            continue
        if h != "n" and t == "n":
            continue
        if h != "n" and t != "n" and h != t:
            continue
        raise ValueError("Seed 中存在非法格点状态。")


@dataclass
class Arrow:
    id: int
    path: list[dict]
    head: dict
    move_dir: str


def parse_arrows(grid: list[int]) -> tuple[list[Arrow], dict[int, int]]:
    arrows: list[Arrow] = []
    occupancy: dict[int, int] = {}
    tails = []
    non_empty = 0
    for r in range(ROWS):
        for c in range(COLS):
            v = grid[idx(r, c)]
            h, t = dirs_of(v)
            if h == "n" and t == "n":
                continue
            non_empty += 1
            if h != "n" and t == "n":
                tails.append({"r": r, "c": c})

    arrow_id = 0
    for tail in tails:
        path: list[dict] = []
        local = set()
        cur = dict(tail)
        for _ in range(POINTS + 2):
            key = idx(cur["r"], cur["c"])
            if key in local:
                raise ValueError("箭头路径成环。")
            local.add(key)
            path.append(dict(cur))
            head_side, tail_side = dirs_of(grid[key])
            if len(path) == 1:
                if tail_side != "n" or head_side == "n":
                    raise ValueError("箭头尾部数据错误。")
            else:
                prev = path[-2]
                expected_tail = dir_between(cur, prev)
                if tail_side != expected_tail:
                    raise ValueError("箭头路径断裂。")
            if head_side == "n":
                break
            d = DELTA[head_side]
            nxt = {"r": cur["r"] + d[0], "c": cur["c"] + d[1]}
            if not in_bounds(nxt["r"], nxt["c"]):
                raise ValueError("箭头路径越界。")
            cur = nxt
        head = path[-1]
        hh, ht = dirs_of(grid[idx(head["r"], head["c"])])
        if hh != "n" or ht == "n":
            raise ValueError("箭头头部数据错误。")
        move_dir = OPP[ht]
        arrow = Arrow(arrow_id, path, dict(head), move_dir)
        for p in path:
            key = idx(p["r"], p["c"])
            if key in occupancy:
                raise ValueError("两个箭头发生重叠。")
            occupancy[key] = arrow.id
        arrows.append(arrow)
        arrow_id += 1

    if len(occupancy) != non_empty:
        raise ValueError("存在无法归属到箭头的孤立格点。")

    for arrow in arrows:
        d = DELTA[arrow.move_dir]
        r = arrow.head["r"] + d[0]
        c = arrow.head["c"] + d[1]
        while in_bounds(r, c):
            if occupancy.get(idx(r, c)) == arrow.id:
                raise ValueError("箭头头部前方被自己的身体挡住，属于非法关卡。")
            r += d[0]
            c += d[1]
    return arrows, occupancy


def can_move(arrow: Arrow, occupancy: dict[int, int]) -> bool:
    d = DELTA[arrow.move_dir]
    r = arrow.head["r"] + d[0]
    c = arrow.head["c"] + d[1]
    while in_bounds(r, c):
        if idx(r, c) in occupancy:
            return False
        r += d[0]
        c += d[1]
    return True


def solve_grid(grid: list[int]) -> tuple[bool, list[list[tuple[int, int]]]]:
    copy = list(grid)
    steps = []
    for _ in range(600):
        try:
            arrows, occ = parse_arrows(copy)
        except Exception:
            return False, steps
        if not arrows:
            return True, steps
        movable = [a for a in arrows if can_move(a, occ)]
        if not movable:
            return False, steps
        movable.sort(key=lambda a: len(a.path))
        chosen = movable[0]
        steps.append([(p["r"], p["c"]) for p in chosen.path])
        for p in chosen.path:
            copy[idx(p["r"], p["c"])] = 0
    return False, steps


def decode_seed(seed: str) -> tuple[list[int], int, list[Arrow], dict[int, int]]:
    clean = normalize_seed(seed)
    expected = 3 + POINTS + 4
    if len(clean) != expected:
        raise ValueError(f"Seed 长度错误，应为 {expected} 个有效字符。")
    time_part = clean[:3]
    try:
        limit_time = int(time_part, 36)
    except Exception:
        limit_time = -1
    if (limit_time < 300 or limit_time > 900) and time_part.isdigit():
        limit_time = int(time_part)
    if limit_time < 300 or limit_time > 900:
        raise ValueError("Seed 中的时间限制不合法。有效范围是 5 到 15 分钟。")
    body = clean[3:3 + POINTS]
    check = clean[3 + POINTS:]
    if check != checksum(time_part + body):
        raise ValueError("Seed 校验失败，可能是复制不完整或被改过。")
    grid = []
    for i, ch in enumerate(body):
        enc = ALPHABET.find(ch)
        if enc < 0:
            raise ValueError("Seed 中含有非法字符。")
        grid.append((enc - seed_key(limit_time, i) + 25) % 25)
    validate_grid_values(grid)
    arrows, occ = parse_arrows(grid)
    ok, _ = solve_grid(grid)
    if not ok:
        raise ValueError("这个 Seed 可以解码，但关卡无解，可能已被篡改。")
    return grid, limit_time, arrows, occ


class JSRandom:
    def __init__(self, seed: int):
        self.s = seed & 0xFFFFFFFF

    def __call__(self) -> float:
        self.s = (self.s * 1664525 + 1013904223) & 0xFFFFFFFF
        return self.s / 4294967296


def rand_int(rng: JSRandom, n: int) -> int:
    return int(rng() * n)


def shuffle_array(rng: JSRandom, arr: list) -> list:
    for i in range(len(arr) - 1, 0, -1):
        j = rand_int(rng, i + 1)
        arr[i], arr[j] = arr[j], arr[i]
    return arr


def random_weighted_choice(rng: JSRandom, items: list[dict]) -> str:
    total = sum(item["weight"] for item in items)
    x = rng() * total
    for item in items:
        x -= item["weight"]
        if x <= 0:
            return item["value"]
    return items[-1]["value"]


def boundary_candidates() -> list[dict]:
    out = []
    for c in range(COLS):
        out.append({"head": {"r": 0, "c": c}, "moveDir": "u", "inDir": "d"})
        out.append({"head": {"r": ROWS - 1, "c": c}, "moveDir": "d", "inDir": "u"})
    for r in range(1, ROWS - 1):
        out.append({"head": {"r": r, "c": 0}, "moveDir": "l", "inDir": "r"})
        out.append({"head": {"r": r, "c": COLS - 1}, "moveDir": "r", "inDir": "l"})
    return out


def place_path(grid: list[int], path: list[dict]) -> bool:
    for i, p in enumerate(path):
        h, t = "n", "n"
        if i < len(path) - 1:
            h = dir_between(path[i], path[i + 1])
        if i > 0:
            t = dir_between(path[i], path[i - 1])
        if h is None or t is None:
            return False
        grid[idx(p["r"], p["c"])] = code_of(h, t)
    return True


def split_worm_into_segments(rng: JSRandom, head_to_tail: list[dict], difficulty: int) -> list[list[dict]]:
    segments = []
    start = 0
    min_chunk = 3 if difficulty >= 3 else (4 if difficulty == 2 else 5)
    max_chunk = 6 if difficulty >= 3 else (9 if difficulty == 2 else 12)
    while start < len(head_to_tail) - 1:
        remaining = len(head_to_tail) - start
        if remaining <= max_chunk + 1:
            length = remaining
        else:
            length = min_chunk + rand_int(rng, max_chunk - min_chunk + 1)
        if remaining - length == 1:
            length += 1
        seg = head_to_tail[start:min(len(head_to_tail), start + length)]
        if len(seg) >= 2:
            segments.append(seg)
        start += length
    return segments


def count_turns(arrows: list[Arrow]) -> int:
    total = 0
    for a in arrows:
        for i in range(1, len(a.path) - 1):
            d1 = dir_between(a.path[i], a.path[i - 1])
            d2 = dir_between(a.path[i], a.path[i + 1])
            if d1 and d2 and OPP[d1] != d2:
                total += 1
    return total


def grow_boundary_worm_grid(seed: int, difficulty: int) -> list[int]:
    dirs = ["u", "d", "l", "r"]
    for restart in range(600):
        rng = JSRandom((seed + restart * 2654435761) & 0xFFFFFFFF)
        if difficulty <= 1:
            target = round(POINTS * 0.30)
            worms_wanted = 10 + rand_int(rng, 4)
        elif difficulty == 2:
            target = round(POINTS * 0.60)
            worms_wanted = 17 + rand_int(rng, 6)
        else:
            target = round(POINTS * 0.97)
            worms_wanted = 34 + rand_int(rng, 10)

        used = set()
        worms = []
        starts = shuffle_array(rng, boundary_candidates())
        for ca in starts:
            if len(worms) >= worms_wanted:
                break
            h = ca["head"]
            d = DELTA[ca["inDir"]]
            nxt = {"r": h["r"] + d[0], "c": h["c"] + d[1]}
            if idx(h["r"], h["c"]) in used or not in_bounds(nxt["r"], nxt["c"]) or idx(nxt["r"], nxt["c"]) in used:
                continue
            worms.append({"headToTail": [dict(h), nxt], "lastDir": ca["inDir"]})
            used.add(idx(h["r"], h["c"]))
            used.add(idx(nxt["r"], nxt["c"]))
        if len(worms) < 4:
            continue
        active = list(worms)
        guard = 0
        while len(used) < target and active and guard < 5000:
            guard += 1
            wi = rand_int(rng, len(active))
            worm = active[wi]
            cur = worm["headToTail"][-1]
            prev = worm["headToTail"][-2]
            backward = dir_between(cur, prev)
            choices = []
            for d in dirs:
                if d == backward:
                    continue
                dd = DELTA[d]
                nr, nc = cur["r"] + dd[0], cur["c"] + dd[1]
                key = idx(nr, nc)
                if not in_bounds(nr, nc) or key in used:
                    continue
                free_neighbors = 0
                for nd in dirs:
                    ndv = DELTA[nd]
                    ar, ac = nr + ndv[0], nc + ndv[1]
                    if in_bounds(ar, ac) and idx(ar, ac) not in used:
                        free_neighbors += 1
                edge_penalty = 0.25 if (nr == 0 or nr == ROWS - 1 or nc == 0 or nc == COLS - 1) else 1
                choices.append({"value": d, "weight": (1 + (2.5 if d != worm["lastDir"] else 0) + free_neighbors * 0.45) * edge_penalty})
            if not choices:
                active.pop(wi)
                continue
            chosen = random_weighted_choice(rng, choices)
            cd = DELTA[chosen]
            nxt = {"r": cur["r"] + cd[0], "c": cur["c"] + cd[1]}
            worm["headToTail"].append(nxt)
            worm["lastDir"] = chosen
            used.add(idx(nxt["r"], nxt["c"]))

        grid = [0] * POINTS
        failed = False
        for worm in worms:
            if len(worm["headToTail"]) < 2:
                continue
            pieces = split_worm_into_segments(rng, list(worm["headToTail"]), difficulty)
            for piece in pieces:
                if not place_path(grid, list(reversed(piece))):
                    failed = True
                    break
            if failed:
                break
        if failed:
            continue
        try:
            arrows, occ = parse_arrows(grid)
            ok, _ = solve_grid(grid)
            if not ok:
                continue
            occ_size = len(occ)
            if difficulty <= 1 and not (90 <= occ_size <= 110):
                continue
            if difficulty == 2 and not (185 <= occ_size <= 210):
                continue
            if difficulty >= 3 and occ_size < int(POINTS * 0.95):
                continue
            internal_heads = sum(1 for a in arrows if 0 < a.head["r"] < ROWS - 1 and 0 < a.head["c"] < COLS - 1)
            initially_blocked = sum(1 for a in arrows if not can_move(a, occ))
            if difficulty >= 3 and (internal_heads < 16 or initially_blocked < 16):
                continue
            if difficulty == 2 and (internal_heads < 8 or initially_blocked < 6):
                continue
            return grid
        except Exception:
            continue
    raise RuntimeError("关卡生成失败，请重试。")


def generate_grid(seed: int, difficulty: int) -> list[int]:
    return grow_boundary_worm_grid(seed, difficulty)


def generate_random_seed() -> str:
    base = (int(time.time() * 1000) ^ random.randint(0, 0xFFFFFFFF)) & 0xFFFFFFFF
    for attempt in range(500):
        rng = JSRandom(base + attempt * 41)
        difficulty = 3 + rand_int(rng, 98)
        grid = generate_grid((base + attempt * 9973) & 0xFFFFFFFF, difficulty)
        try:
            arrows, occ = parse_arrows(grid)
        except Exception:
            continue
        if len(occ) < int(POINTS * 0.95):
            continue
        ok, _ = solve_grid(grid)
        if not ok:
            continue
        turns = count_turns(arrows)
        limit_time = max(300, min(900, round(360 + len(arrows) * 8 + turns * 4 + len(occ) * 0.75)))
        return encode_seed(grid, limit_time)
    raise RuntimeError("随机关卡生成失败，请再试一次。")


