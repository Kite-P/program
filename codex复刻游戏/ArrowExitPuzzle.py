    def draw_arrow_head(self, surf, path, move_dir, ox, oy, highlighted=False, alpha=1.0) -> None:
        p = path[-1]
        x, y = self.point_pos(p["r"], p["c"])
        if len(path) >= 2:
            a, b = path[-2], path[-1]
            dr, dc = b["r"] - a["r"], b["c"] - a["c"]
        else:
            dr, dc = DELTA[move_dir]
        length = math.hypot(dr, dc) or 1
        nr, nc = dr / length, dc / length
        size = max(7.0, 8.8 * self.zoom)
        tip = (x + ox + nc * size * 0.58, y + oy + nr * size * 0.58)
        perp = (-nr, nc)
        back = (tip[0] - nc * size * 1.42, tip[1] - nr * size * 1.42)
        p1 = tip
        p2 = (back[0] + perp[0] * size * 0.58, back[1] + perp[1] * size * 0.58)
        p3 = (back[0] - perp[0] * size * 0.58, back[1] - perp[1] * size * 0.58)
        if highlighted:
            pygame.draw.polygon(surf, (255, 196, 0, int(230 * alpha)), [p1, p2, p3])
        pygame.draw.polygon(surf, (42, 45, 48, int(255 * alpha)), [p1, p2, p3])

    def build_exit_route(self, arrow: Arrow) -> list[dict]:
        route = [dict(p) for p in arrow.path]
        d = DELTA[arrow.move_dir]
        cur = dict(arrow.head)
        extra = max(ROWS, COLS) + len(arrow.path) + 4
        for _ in range(extra):
            cur = {"r": cur["r"] + d[0], "c": cur["c"] + d[1]}
            route.append(dict(cur))
        return route

    @staticmethod
    def sample_route(route, s: float) -> dict:
        if s <= 0:
            return dict(route[0])
        i = int(math.floor(s))
        f = s - i
        if i >= len(route) - 1:
            return dict(route[-1])
        a, b = route[i], route[i + 1]
        return {"r": a["r"] + (b["r"] - a["r"]) * f, "c": a["c"] + (b["c"] - a["c"]) * f}

    def draw_exiting_arrow(self, arrow: Arrow, t: float) -> None:
        route = self.build_exit_route(arrow)
        visible = len(arrow.path)
        max_shift = max(1, len(route) - visible)
        eased = 1 - (1 - min(1, t)) ** 2.35
        shift = eased * max_shift
        dyn = [self.sample_route(route, shift + i) for i in range(visible)]
        alpha = 1 if t < 0.86 else max(0, 1 - (t - 0.86) / 0.14)
        self.draw_path_shape(dyn, arrow.move_dir, arrow.id, 0, 0, alpha)

