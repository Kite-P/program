    def draw_wrapped_text(self, text: str, x: int, y: int, max_w: int, font, color, line_h: int, max_lines: int = 2) -> None:
        if not text:
            return
        # Simple mixed CJK/ASCII line wrap by rendered width. Keep punctuation
        # such as Chinese full stops attached to the previous line, so a lone
        # punctuation mark will not appear on a new line.
        lines = []
        cur = ""
        punctuation = "。，,.!?！？；;：:"
        for ch in text:
            test = cur + ch
            if font.size(test)[0] <= max_w or (ch in punctuation and cur):
                cur = test
            else:
                if cur:
                    lines.append(cur)
                cur = ch
                if len(lines) >= max_lines:
                    break
        if cur and len(lines) < max_lines:
            lines.append(cur)
        if len(lines) == max_lines and len("".join(lines)) < len(text):
            while lines[-1] and font.size(lines[-1] + "...")[0] > max_w:
                lines[-1] = lines[-1][:-1]
            lines[-1] += "..."
        for i, line in enumerate(lines):
            surf = font.render(line, True, color)
            self.screen.blit(surf, (x, y + i * line_h))

